class Solution:
    def numDecodings(self, s: str) -> int:
        """
        dp[i] 表示：前 i 个字符 s[0 .. i-1] 的解码方式数量
        注意 i 是“长度”，不是下标
        """
        if not s:
            return 0
        n = len(s)
        # dp[0..n]
        dp = [0] * (n + 1)
        # 空字符串：1 种“什么都不选”的解码方式
        # 不是“解码出一个字母”，而是：作为 dp[i-2] 的合法起点（数学上的 1）
        dp[0] = 1
        # 只有 1 个字符时，是否能单独解码
        if s[0] != '0':  # '1'~'9' -> 1 
            dp[1] = 1
        else:
            dp[1] = 0    # '0'-> 0
        # 从第 2 个字符开始
        for i in range(2, n + 1):
            # ---------- 情况 1：最后 1 位单独解码 ----------
            # s[i-1] 是当前最后一个字符
            # 只要不是 '0'，就可以作为一个字母
            if s[i - 1] != '0':
                dp[i] += dp[i - 1]
            # ---------- 情况 2：最后 2 位一起解码 ----------
            # 看 s[i-2 : i] 这个两位数是否在 10 ~ 26
            two_digit = int(s[i - 2 : i])
            if 10 <= two_digit <= 26:
                dp[i] += dp[i - 2]

        return dp[n]
